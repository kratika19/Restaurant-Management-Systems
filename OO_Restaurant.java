        itemCost.hakkaNoodles = itemCost.phakkaNoodles * Double.parseDouble(jTNoodles.getText());
        itemCost.vegChesseSandwich = itemCost.pvegChesseSandwich * Double.parseDouble(jTSandwich.getText());
        itemCost.specialBurger = itemCost.pspecialBurger * Double.parseDouble(jTBurger.getText());
        
        isubtotal = itemCost.GetAmount();
