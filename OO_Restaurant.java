        // TODO add your handling code here:
        SubClass_Child itemCost = new SubClass_Child();
        
        double itax, itotal , isubtotal;
        
        itemCost.chocolateMilkshake = itemCost.pchocolateMilkshake * Double.parseDouble(jTChocMilkshake.getText());
        itemCost.iceTea = itemCost.piceTea * Double.parseDouble(jTIceTea.getText());
